package com.hz.config;

/**
 * @author hz
 * @create 2021-01-13
 */
public class RedisConf {

    private String ip;

    private int port;

    private int num;

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public int getPort() {
        return port;
    }

    public void setPort(int port) {
        this.port = port;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }
}
